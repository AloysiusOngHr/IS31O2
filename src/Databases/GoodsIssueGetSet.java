package Databases;

public class GoodsIssueGetSet {

	int goodsIssueId;
	String dateCreated;
	String dateIssud;
	String createdBy;
	
	public int getGoodsIssueId() {
		return goodsIssueId;
	}
	public void setGoodsIssueId(int goodsIssueId) {
		this.goodsIssueId = goodsIssueId;
	}
	public String getDateCreated() {
		return dateCreated;
	}
	public void setDateCreated(String dateCreated) {
		this.dateCreated = dateCreated;
	}
	public String getDateIssud() {
		return dateIssud;
	}
	public void setDateIssud(String dateIssud) {
		this.dateIssud = dateIssud;
	}
	public String getCreatedBy() {
		return createdBy;
	}
	public void setCreatedBy(String createdBy) {
		this.createdBy = createdBy;
	}

}
